package pl.edu.pjatk.kotlinexercise

/**
 * Exercise: Explicit Variable Type Declaration
 * Explicitly declare the correct type for each variable:
 * - Variable 'a' stores an integer value.
 * - Variable 'b' stores a string value.
 * - Variable 'c' stores a double value.
 * - Variable 'd' stores a long value.
 * - Variable 'e' stores a boolean value.
 * - Variable 'f' stores a character value.
 */
fun main() {
    // Variable 'a' stores an integer value
    val a: Int = 1000
    // Variable 'b' stores a string value
    val b: String = "log message"
    // Variable 'c' stores a double value
    val c: Double = 3.14
    // Variable 'd' stores a long value
    val d: Long = 100_000_000_000
    // Variable 'e' stores a boolean value
    val e: Boolean = false
    // Variable 'f' stores a character value
    val f: Char = '\n'
}
