package pl.edu.pjatk.kotlinexercise

/**
 * Exercise: Print Name and Age
 * Complete the code to make the program print "Mary is 20 years old" to standard output.
 *
 * The program has two variables:
 * - 'name' stores the name "Mary".
 * - 'age' stores the age 20.
 *
 * The goal is to print the name and age in the format: "<name> is <age> years old".
 */
fun main() {
    // The name of the person
    val name = "Mary"
    // The age of the person
    val age = 20

    // Print the name and age in the specified format
    println("$name is $age years old")
}
